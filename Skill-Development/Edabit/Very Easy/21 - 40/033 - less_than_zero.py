"""
Is the Number Less than or Equal to Zero?

Create a function that takes a number as it's only argument and returns True if it's less than or equal to zero,
otherwise return False
"""

def less_than_or_equal_to_zero(x):
    if x <= 0:
        print("True")
    else:
        print("False")


less_than_or_equal_to_zero(5)
less_than_or_equal_to_zero(0)
less_than_or_equal_to_zero(-2)
