def is_plural(word):
    if 's' in word:
        print(f'Word: {word}')
        print(f'Plural: {True}\n')
    else:
        print(f'Word: {word}')
        print(f'Plural: {False}\n')


is_plural('change')
is_plural('dudes')

is_plural('flowers')
is_plural('checks')

is_plural('varies')
is_plural('efforts')

is_plural('mood')
is_plural('whiteboard')

is_plural('cow')
is_plural('word')

is_plural('love')
is_plural('silly')
