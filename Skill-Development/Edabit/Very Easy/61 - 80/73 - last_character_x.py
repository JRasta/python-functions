def is_last_character(name):
    if name[-1] == 'n':
        print(f'Name      : {name}')
        print(f'Contains N: {True}\n')
    else:
        print(f'Name      : {name}')
        print(f'Contains N: {False}\n')


is_last_character('Aiden')
is_last_character('Roxy')
