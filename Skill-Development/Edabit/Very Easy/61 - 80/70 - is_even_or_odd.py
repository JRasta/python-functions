def is_even_or_odd(number):
    if number % 2 == 0:
        print(f'Number asked: {number}')
        return print(f'Is it Even ==> {True}\n')
    else:
        print(f'Number asked: {number}')
        return print(f'Is it Even ==> {False}\n')


