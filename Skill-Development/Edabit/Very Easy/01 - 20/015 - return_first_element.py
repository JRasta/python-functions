"""
Return the First Element in a List

Create a function that takes a list containing only numbers and return the first element
"""

def get_first_value(u_list):
    first_element = u_list[0]
    print(f'First element in the list {u_list} is: {first_element}')


get_first_value([1, 2, 3])
get_first_value([80, 5, 100])
get_first_value([-500, 0, 50])
