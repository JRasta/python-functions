"""
Find the Perimeter of a Rectangle

Create a function that takes length and width and finds the perimeter of a rectangle
"""

def find_perimeter(x, y):
    perimeter = (x + y) * 2
    print(f'L:{x} & W:{y} = Perimeter:{perimeter}')


find_perimeter(6, 7)
find_perimeter(20, 10)
find_perimeter(2, 9)
